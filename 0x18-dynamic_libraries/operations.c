#include <stdio.h>
/**
 * add - addition operation
 * @a: number one
 * @b: number two
 * Return: the + of the numbers
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - Subtraction operation
 * @a: number one
 * @b: number two
 * Return: the - of the numbers
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplication operation
 * @a: number one
 * @b: number two
 * Return: the * of the numbers
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - division operation
 * @a: number one
 * @b: number two
 * Return: the / of the numbers
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - %
 * @a: number one
 * @b: number two
 * Return: the % of the numbers
 */
int mod(int a, int b)
{
	return (a % b);
}
